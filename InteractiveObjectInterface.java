/**
 * [InteractiveObjectInterface.java]
 * interface for InteractiveObject
 */

public interface InteractiveObjectInterface {
    /**
     * getMame
     * gets name of object
     * @return name of objects
     */
    abstract String getName();

    /**
     * gettype
     * get type of object
     * @return type of object
     */
    abstract int getType();
}
